# Faça um algoritmo que leia o salario de um funcionario e mostre seu novo salario, com 15% de aumento
salario = int(input("Digite seu salario atual: ")) # Escreva seu salario
aumento = salario + (salario * 0.15)
print(f'O salário com valor de {salario}, com aumento de 15% será equivalente a {aumento} reais!')